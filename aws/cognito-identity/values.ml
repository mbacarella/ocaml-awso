module IdentityDescription =
  struct
    type nonrec t =
      {
      identityId: IdentityId.t option ;
      logins: LoginsList.t option ;
      creationDate: DateType.t option ;
      lastModifiedDate: DateType.t option }
    type nonrec error =
      [ `InternalErrorException of InternalErrorException.t 
      | `InvalidParameterException of InvalidParameterException.t 
      | `NotAuthorizedException of NotAuthorizedException.t 
      | `ResourceNotFoundException of ResourceNotFoundException.t 
      | `TooManyRequestsException of TooManyRequestsException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?identityId =
      fun ?logins ->
        fun ?creationDate ->
          fun ?lastModifiedDate ->
            fun () -> { identityId; logins; creationDate; lastModifiedDate }
    let error_of_json name json =
      match name with
      | "InternalErrorException" ->
          `InternalErrorException (InternalErrorException.of_json json)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_json json)
      | "NotAuthorizedException" ->
          `NotAuthorizedException (NotAuthorizedException.of_json json)
      | "ResourceNotFoundException" ->
          `ResourceNotFoundException (ResourceNotFoundException.of_json json)
      | "TooManyRequestsException" ->
          `TooManyRequestsException (TooManyRequestsException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "InternalErrorException" ->
          `InternalErrorException (InternalErrorException.of_xml xml)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_xml xml)
      | "NotAuthorizedException" ->
          `NotAuthorizedException (NotAuthorizedException.of_xml xml)
      | "ResourceNotFoundException" ->
          `ResourceNotFoundException (ResourceNotFoundException.of_xml xml)
      | "TooManyRequestsException" ->
          `TooManyRequestsException (TooManyRequestsException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `InternalErrorException e ->
          `Assoc
            [("error", (`String "InternalErrorException"));
            ("details", (InternalErrorException.to_json e))]
      | `InvalidParameterException e ->
          `Assoc
            [("error", (`String "InvalidParameterException"));
            ("details", (InvalidParameterException.to_json e))]
      | `NotAuthorizedException e ->
          `Assoc
            [("error", (`String "NotAuthorizedException"));
            ("details", (NotAuthorizedException.to_json e))]
      | `ResourceNotFoundException e ->
          `Assoc
            [("error", (`String "ResourceNotFoundException"));
            ("details", (ResourceNotFoundException.to_json e))]
      | `TooManyRequestsException e ->
          `Assoc
            [("error", (`String "TooManyRequestsException"));
            ("details", (TooManyRequestsException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("IdentityId", (Option.map x.identityId ~f:IdentityId.to_value));
        ("Logins", (Option.map x.logins ~f:LoginsList.to_value));
        ("CreationDate", (Option.map x.creationDate ~f:DateType.to_value));
        ("LastModifiedDate",
          (Option.map x.lastModifiedDate ~f:DateType.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let lastModifiedDate =
        (Option.map ~f:DateType.of_xml)
          (Xml.child xml_arg0 "LastModifiedDate") in
      let creationDate =
        (Option.map ~f:DateType.of_xml) (Xml.child xml_arg0 "CreationDate") in
      let logins =
        (Option.map ~f:LoginsList.of_xml) (Xml.child xml_arg0 "Logins") in
      let identityId =
        (Option.map ~f:IdentityId.of_xml) (Xml.child xml_arg0 "IdentityId") in
      make ?lastModifiedDate ?creationDate ?logins ?identityId ()
    let of_json json =
      let lastModifiedDate =
        field_map json "LastModifiedDate" DateType.of_json in
      let creationDate = field_map json "CreationDate" DateType.of_json in
      let logins = field_map json "Logins" LoginsList.of_json in
      let identityId = field_map json "IdentityId" IdentityId.of_json in
      make ?lastModifiedDate ?creationDate ?logins ?identityId ()
    let to_json v = composed_to_json to_value v
  end
