      maxIdleTimeInSeconds: OptionalInteger.t option ;
      timeoutInSeconds: OptionalInteger.t option ;
      statusTimeoutInSeconds: OptionalInteger.t option ;
      pinned: OptionalBoolean.t option ;
      inputPayloadEncodingType: LambdaInputPayloadEncodingType.t option ;
      execArgs: LambdaExecArgsList.t option ;
      environmentVariables: LambdaEnvironmentVariables.t option ;
      linuxProcessParams: LambdaLinuxProcessParams.t option }
    let make ?eventSources =
      fun ?maxQueueSize ->
        fun ?maxInstancesCount ->
          fun ?maxIdleTimeInSeconds ->
            fun ?timeoutInSeconds ->
              fun ?statusTimeoutInSeconds ->
                fun ?pinned ->
                  fun ?inputPayloadEncodingType ->
                    fun ?execArgs ->
                      fun ?environmentVariables ->
                        fun ?linuxProcessParams ->
                          fun () ->
                            {
                              eventSources;
                              maxQueueSize;
                              maxInstancesCount;
                              maxIdleTimeInSeconds;
                              timeoutInSeconds;
                              statusTimeoutInSeconds;
                              pinned;
                              inputPayloadEncodingType;
                              execArgs;
                              environmentVariables;
                              linuxProcessParams
                            }
    let to_value x =
      structure_to_value
        [("eventSources",
           (Option.map x.eventSources ~f:LambdaEventSourceList.to_value));
        ("maxQueueSize",
          (Option.map x.maxQueueSize ~f:OptionalInteger.to_value));
        ("maxInstancesCount",
          (Option.map x.maxInstancesCount ~f:OptionalInteger.to_value));
        ("maxIdleTimeInSeconds",
          (Option.map x.maxIdleTimeInSeconds ~f:OptionalInteger.to_value));
        ("timeoutInSeconds",
          (Option.map x.timeoutInSeconds ~f:OptionalInteger.to_value));
        ("statusTimeoutInSeconds",
          (Option.map x.statusTimeoutInSeconds ~f:OptionalInteger.to_value));
        ("pinned", (Option.map x.pinned ~f:OptionalBoolean.to_value));
        ("inputPayloadEncodingType",
          (Option.map x.inputPayloadEncodingType
             ~f:LambdaInputPayloadEncodingType.to_value));
        ("execArgs", (Option.map x.execArgs ~f:LambdaExecArgsList.to_value));
        ("environmentVariables",
          (Option.map x.environmentVariables
             ~f:LambdaEnvironmentVariables.to_value));
        ("linuxProcessParams",
          (Option.map x.linuxProcessParams
             ~f:LambdaLinuxProcessParams.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let linuxProcessParams =
        (Option.map ~f:LambdaLinuxProcessParams.of_xml)
          (Xml.child xml_arg0 "linuxProcessParams") in
      let environmentVariables =
        (Option.map ~f:LambdaEnvironmentVariables.of_xml)
          (Xml.child xml_arg0 "environmentVariables") in
      let execArgs =
        (Option.map ~f:LambdaExecArgsList.of_xml)
          (Xml.child xml_arg0 "execArgs") in
      let inputPayloadEncodingType =
        (Option.map ~f:LambdaInputPayloadEncodingType.of_xml)
          (Xml.child xml_arg0 "inputPayloadEncodingType") in
      let pinned =
        (Option.map ~f:OptionalBoolean.of_xml) (Xml.child xml_arg0 "pinned") in
      let statusTimeoutInSeconds =
        (Option.map ~f:OptionalInteger.of_xml)
          (Xml.child xml_arg0 "statusTimeoutInSeconds") in
      let timeoutInSeconds =
        (Option.map ~f:OptionalInteger.of_xml)
          (Xml.child xml_arg0 "timeoutInSeconds") in
      let maxIdleTimeInSeconds =
        (Option.map ~f:OptionalInteger.of_xml)
          (Xml.child xml_arg0 "maxIdleTimeInSeconds") in
      let maxInstancesCount =
        (Option.map ~f:OptionalInteger.of_xml)
          (Xml.child xml_arg0 "maxInstancesCount") in
      let maxQueueSize =
        (Option.map ~f:OptionalInteger.of_xml)
          (Xml.child xml_arg0 "maxQueueSize") in
      let eventSources =
        (Option.map ~f:LambdaEventSourceList.of_xml)
          (Xml.child xml_arg0 "eventSources") in
