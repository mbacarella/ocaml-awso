    type nonrec t =
      | REQUESTED 
      | INITIATED 
      | DEPLOYABLE 
      | FAILED 
      | DEPRECATED 
      | Non_static_id of string 
    let make i = i
    let to_string =
      function
      | REQUESTED -> "REQUESTED"
      | INITIATED -> "INITIATED"
      | DEPLOYABLE -> "DEPLOYABLE"
      | FAILED -> "FAILED"
      | DEPRECATED -> "DEPRECATED"
      | Non_static_id s -> s
    let of_string =
      function
      | "REQUESTED" -> REQUESTED
      | "INITIATED" -> INITIATED
      | "DEPLOYABLE" -> DEPLOYABLE
      | "FAILED" -> FAILED
      | "DEPRECATED" -> DEPRECATED
      | x -> Non_static_id x
    let to_value x = `Enum (to_string x)
    let to_query v = to_query to_value v
    let to_header x = to_string x
    let of_xml xml_arg0 =
      of_string
        (string_of_xml ~kind:"enumeration CloudComponentState" xml_arg0)
    let of_json j = of_string (string_of_json ~kind:"CloudComponentState" j)
    let to_json = simple_to_json to_value
  end
module StringMap =
  struct
    type nonrec t = (NonEmptyString.t * NonEmptyString.t) list
    let make i = i
    let of_header xs =
      make
        (List.filter_map xs
           ~f:(fun (k, v) ->
                 (Base.String.chop_prefix k ~prefix:"x-amz-meta-") |>
                   (Option.map
                      ~f:(fun chopped ->
                            ((NonEmptyString.of_string chopped),
                              (NonEmptyString.of_string v))))))
    let to_value xs =
      (xs |>
         (List.map
            ~f:(fun (x, y) ->
                  (NonEmptyString.to_value x) |>
                    (fun x ->
                       (NonEmptyString.to_value y) |> (fun y -> (x, y))))))
        |> (fun x -> `Map x)
    let to_query v = to_query to_value v
    let of_xml _ =
      failwith "of_xml_converter_of_shape: Map_shape case not implemented"
    let of_json j =
      object_of_json ~key_of_string:NonEmptyString.of_string
        ~of_json:NonEmptyString.of_json j
    let to_json v = composed_to_json to_value v
  end
