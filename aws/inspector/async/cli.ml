    ([%map_open.Command
       let cli_profile =
         flag "-cli-profile" (optional string) ~doc:"NAME aws profile to use"
       and cli_region =
         flag "-cli-region" (optional string) ~doc:"REGION override region"
       and endpoint_url =
         flag "-endpoint-url" (optional string)
           ~doc:"URL override endpoint url"
       and assessmentRunArns =
         flag "assessment-run-arns" (optional json_arg)
           ~doc:"JSON ListParentArnList"
       and filter =
         flag "filter" (optional json_arg) ~doc:"JSON FindingFilter"
       and nextToken =
         flag "next-token" (optional string) ~doc:"STRING PaginationToken"
       and maxResults =
         flag "max-results" (optional int) ~doc:"INT ListMaxResults" in
       fun () ->
         call ?endpoint_url ?profile:cli_profile ?region:cli_region
           Io.list_findings
           (Values.ListFindingsRequest.make
              ?assessmentRunArns:(Option.map
                                    ~f:Values.ListParentArnList.of_json
                                    assessmentRunArns)
              ?filter:(Option.map ~f:Values.FindingFilter.of_json filter)
              ?nextToken ?maxResults ())
           (Some Values.ListFindingsResponse.to_json)
           (Some Values.ListFindingsResponse.error_to_json)])
