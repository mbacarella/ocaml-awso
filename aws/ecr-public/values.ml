      | "LimitExceededException" ->
          `LimitExceededException (LimitExceededException.of_json json)
      | "RegistryNotFoundException" ->
          `RegistryNotFoundException (RegistryNotFoundException.of_json json)
      | "RepositoryNotFoundException" ->
          `RepositoryNotFoundException
            (RepositoryNotFoundException.of_json json)
      | "ServerException" -> `ServerException (ServerException.of_json json)
      | "UnsupportedCommandException" ->
          `UnsupportedCommandException
            (UnsupportedCommandException.of_json json)
      | "UploadNotFoundException" ->
          `UploadNotFoundException (UploadNotFoundException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "InvalidLayerPartException" ->
          `InvalidLayerPartException (InvalidLayerPartException.of_xml xml)
