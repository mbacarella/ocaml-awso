      function
      | `InvalidLayerPartException e ->
          `Assoc
            [("error", (`String "InvalidLayerPartException"));
            ("details", (InvalidLayerPartException.to_json e))]
      | `InvalidParameterException e ->
          `Assoc
            [("error", (`String "InvalidParameterException"));
            ("details", (InvalidParameterException.to_json e))]
