module Check =
  struct
    type nonrec t =
      {
      type_: CheckType.t option
        [@ocaml.doc
          "The check type. Amazon EVS performs the following checks. KEY_REUSE: checks that the VCF license key is not used by another Amazon EVS environment. This check fails if a used license is added to the environment. KEY_COVERAGE: checks that your VCF license key allocates sufficient vCPU cores for all deployed hosts. The check fails when any assigned hosts in the EVS environment are not covered by license keys, or when any unassigned hosts cannot be covered by available vCPU cores in keys. REACHABILITY: checks that the Amazon EVS control plane has a persistent connection to SDDC Manager. If Amazon EVS cannot reach the environment, this check fails. HOST_COUNT: Checks that your environment has a minimum of 4 hosts. If this check fails, you will need to add hosts so that your environment meets this minimum requirement. Amazon EVS only supports environments with 4-16 hosts."];
      result: CheckResult.t option [@ocaml.doc "The check result."];
      impairedSince: Timestamp.t option
        [@ocaml.doc "The time when environment health began to be impaired."]}
    let make ?type_ =
      fun ?result ->
        fun ?impairedSince -> fun () -> { type_; result; impairedSince }
    let to_value x =
      structure_to_value
        [("type", (Option.map x.type_ ~f:CheckType.to_value));
        ("result", (Option.map x.result ~f:CheckResult.to_value));
        ("impairedSince", (Option.map x.impairedSince ~f:Timestamp.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let impairedSince =
        (Option.map ~f:Timestamp.of_xml) (Xml.child xml_arg0 "impairedSince") in
      let result =
        (Option.map ~f:CheckResult.of_xml) (Xml.child xml_arg0 "result") in
      let type_ =
        (Option.map ~f:CheckType.of_xml) (Xml.child xml_arg0 "type") in
      make ?impairedSince ?result ?type_ ()
    let of_string s = of_xml (Awso.Xml.parse_response s)[@@warning "-32"]
    let of_json json__ =
      let impairedSince = field_map json__ "impairedSince" Timestamp.of_json in
      let result = field_map json__ "result" CheckResult.of_json in
      let type_ = field_map json__ "type" CheckType.of_json in
      make ?impairedSince ?result ?type_ ()
    let to_json v = composed_to_json to_value v
  end[@@ocaml.doc
       "A check on the environment to identify environment health and validate VMware VCF licensing compliance."]
