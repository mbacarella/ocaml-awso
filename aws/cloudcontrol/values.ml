          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `ConcurrentModificationException e ->
          `Assoc
            [("error", (`String "ConcurrentModificationException"));
            ("details", (ConcurrentModificationException.to_json e))]
      | `RequestTokenNotFoundException e ->
          `Assoc
            [("error", (`String "RequestTokenNotFoundException"));
            ("details", (RequestTokenNotFoundException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("ProgressEvent",
           (Option.map x.progressEvent ~f:ProgressEvent.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let progressEvent =
        (Option.map ~f:ProgressEvent.of_xml)
          (Xml.child xml_arg0 "ProgressEvent") in
      make ?progressEvent ()
    let of_json json =
      let progressEvent =
        field_map json "ProgressEvent" ProgressEvent.of_json in
      make ?progressEvent ()
    let to_json v = composed_to_json to_value v
  end
