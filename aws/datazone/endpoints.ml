  let _req = req in
  match endp with
  | AcceptPredictions -> Awso.Http.Request.make (method_of_endpoint endp)
  | AcceptSubscriptionRequest ->
      Awso.Http.Request.make (method_of_endpoint endp)
  | AddEntityOwner ->
      let (headers, body) =
        let headers =
          Some ((List.filter_opt []) |> Awso.Http.Headers.of_list) in
        let body =
          Some
            ((`Assoc
                (List.map
                   (List.filter_opt
                      [Some
                         ("owner",
                           (OwnerProperties.to_value
                              req.AddEntityOwnerInput.owner));
                      Option.map req.AddEntityOwnerInput.clientToken
