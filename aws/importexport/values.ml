    type listJobsResult =
      {
      jobs: JobsList.t option ;
      isTruncated: IsTruncated.t option }
    and responseMetaData = unit
    and t =
      {
      listJobsResult: listJobsResult ;
      responseMetaData: responseMetaData }
    type error =
      [ `InvalidAccessKeyIdException of InvalidAccessKeyIdException.t 
      | `InvalidParameterException of InvalidParameterException.t 
      | `InvalidVersionException of InvalidVersionException.t 
      | `Unknown_operation_error of (string * string option) ]
    let context_ = "ListJobsOutput"
    let make ?jobs =
      fun ?isTruncated ->
        fun () ->
          { listJobsResult = { jobs; isTruncated }; responseMetaData = () }
    let error_of_json name json =
      match name with
      | "InvalidAccessKeyIdException" ->
          `InvalidAccessKeyIdException
            (InvalidAccessKeyIdException.of_json json)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_json json)
      | "InvalidVersionException" ->
          `InvalidVersionException (InvalidVersionException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "InvalidAccessKeyIdException" ->
          `InvalidAccessKeyIdException
            (InvalidAccessKeyIdException.of_xml xml)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_xml xml)
      | "InvalidVersionException" ->
          `InvalidVersionException (InvalidVersionException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `InvalidAccessKeyIdException e ->
          `Assoc
            [("error", (`String "InvalidAccessKeyIdException"));
            ("details", (InvalidAccessKeyIdException.to_json e))]
      | `InvalidParameterException e ->
          `Assoc
            [("error", (`String "InvalidParameterException"));
            ("details", (InvalidParameterException.to_json e))]
      | `InvalidVersionException e ->
          `Assoc
            [("error", (`String "InvalidVersionException"));
            ("details", (InvalidVersionException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value t =
      let x = t.listJobsResult in
      structure_to_wrapped_value
        [("Jobs", (Option.map x.jobs ~f:JobsList.to_value));
        ("IsTruncated", (Option.map x.isTruncated ~f:IsTruncated.to_value))]
        ~wrapper:"ListJobsResult" ~response:"ResponseMetaData"
    let to_query v = to_query to_value v
    let of_xml t =
      let xml_arg0 = Xml.child_exn ~context:context_ t "ListJobsResult" in
      let isTruncated =
        (Option.map ~f:IsTruncated.of_xml) (Xml.child xml_arg0 "IsTruncated") in
      let jobs = (Option.map ~f:JobsList.of_xml) (Xml.child xml_arg0 "Jobs") in
      make ?isTruncated ?jobs ()
    let of_json json =
      let isTruncated = field_map json "IsTruncated" IsTruncated.of_json in
      let jobs = field_map json "Jobs" JobsList.of_json in
      make ?isTruncated ?jobs ()
    let to_json v = composed_to_json to_value v
  end
