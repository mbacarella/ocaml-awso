    let context_ = "CreateEnvironmentRequest"
    let make ?description =
      fun ?kmsKeyId ->
        fun ?tags ->
          fun ?federationMode ->
            fun ?federationParameters ->
              fun ?superuserParameters ->
                fun ?dataBundles ->
                  fun ~name ->
                    fun () ->
                      {
                        description;
                        kmsKeyId;
                        tags;
                        federationMode;
                        federationParameters;
                        superuserParameters;
                        dataBundles;
                        name
                      }
    let to_value x =
      structure_to_value
        [("name", (Some (EnvironmentName.to_value x.name)));
        ("description", (Option.map x.description ~f:Description.to_value));
        ("kmsKeyId", (Option.map x.kmsKeyId ~f:KmsKeyId.to_value));
        ("tags", (Option.map x.tags ~f:TagMap.to_value));
        ("federationMode",
          (Option.map x.federationMode ~f:FederationMode.to_value));
