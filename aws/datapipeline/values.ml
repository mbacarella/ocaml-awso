module ValidatePipelineDefinitionOutput =
  struct
    type nonrec t =
      {
      validationErrors: ValidationErrors.t option ;
      validationWarnings: ValidationWarnings.t option ;
      errored: Boolean.t }
    type nonrec error =
      [ `InternalServiceError of InternalServiceError.t 
      | `InvalidRequestException of InvalidRequestException.t 
      | `PipelineDeletedException of PipelineDeletedException.t 
      | `PipelineNotFoundException of PipelineNotFoundException.t 
      | `Unknown_operation_error of (string * string option) ]
    let context_ = "ValidatePipelineDefinitionOutput"
    let make ?validationErrors =
      fun ?validationWarnings ->
        fun ~errored ->
          fun () -> { validationErrors; validationWarnings; errored }
    let error_of_json name json =
      match name with
      | "InternalServiceError" ->
          `InternalServiceError (InternalServiceError.of_json json)
      | "InvalidRequestException" ->
          `InvalidRequestException (InvalidRequestException.of_json json)
      | "PipelineDeletedException" ->
          `PipelineDeletedException (PipelineDeletedException.of_json json)
      | "PipelineNotFoundException" ->
          `PipelineNotFoundException (PipelineNotFoundException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "InternalServiceError" ->
          `InternalServiceError (InternalServiceError.of_xml xml)
      | "InvalidRequestException" ->
          `InvalidRequestException (InvalidRequestException.of_xml xml)
      | "PipelineDeletedException" ->
          `PipelineDeletedException (PipelineDeletedException.of_xml xml)
      | "PipelineNotFoundException" ->
          `PipelineNotFoundException (PipelineNotFoundException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `InternalServiceError e ->
          `Assoc
            [("error", (`String "InternalServiceError"));
            ("details", (InternalServiceError.to_json e))]
      | `InvalidRequestException e ->
          `Assoc
            [("error", (`String "InvalidRequestException"));
            ("details", (InvalidRequestException.to_json e))]
      | `PipelineDeletedException e ->
          `Assoc
            [("error", (`String "PipelineDeletedException"));
            ("details", (PipelineDeletedException.to_json e))]
      | `PipelineNotFoundException e ->
