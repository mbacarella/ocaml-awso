    type nonrec error =
      [ `AccessDeniedException of AccessDeniedException.t 
      | `InternalServerException of InternalServerException.t 
      | `RequestThrottledException of RequestThrottledException.t 
      | `ResourceNotFoundException of ResourceNotFoundException.t 
      | `ServiceQuotaExceededException of ServiceQuotaExceededException.t 
      | `ValidationException of ValidationException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?status = fun () -> { status }
    let error_of_json name json =
      match name with
      | "AccessDeniedException" ->
          `AccessDeniedException (AccessDeniedException.of_json json)
      | "InternalServerException" ->
          `InternalServerException (InternalServerException.of_json json)
      | "RequestThrottledException" ->
          `RequestThrottledException (RequestThrottledException.of_json json)
      | "ResourceNotFoundException" ->
          `ResourceNotFoundException (ResourceNotFoundException.of_json json)
      | "ServiceQuotaExceededException" ->
          `ServiceQuotaExceededException
            (ServiceQuotaExceededException.of_json json)
      | "ValidationException" ->
          `ValidationException (ValidationException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "AccessDeniedException" ->
          `AccessDeniedException (AccessDeniedException.of_xml xml)
      | "InternalServerException" ->
          `InternalServerException (InternalServerException.of_xml xml)
      | "RequestThrottledException" ->
          `RequestThrottledException (RequestThrottledException.of_xml xml)
      | "ResourceNotFoundException" ->
          `ResourceNotFoundException (ResourceNotFoundException.of_xml xml)
      | "ServiceQuotaExceededException" ->
          `ServiceQuotaExceededException
            (ServiceQuotaExceededException.of_xml xml)
      | "ValidationException" ->
          `ValidationException (ValidationException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `AccessDeniedException e ->
          `Assoc
            [("error", (`String "AccessDeniedException"));
            ("details", (AccessDeniedException.to_json e))]
      | `InternalServerException e ->
          `Assoc
            [("error", (`String "InternalServerException"));
            ("details", (InternalServerException.to_json e))]
      | `RequestThrottledException e ->
          `Assoc
            [("error", (`String "RequestThrottledException"));
            ("details", (RequestThrottledException.to_json e))]
      | `ResourceNotFoundException e ->
          `Assoc
            [("error", (`String "ResourceNotFoundException"));
            ("details", (ResourceNotFoundException.to_json e))]
      | `ServiceQuotaExceededException e ->
          `Assoc
            [("error", (`String "ServiceQuotaExceededException"));
            ("details", (ServiceQuotaExceededException.to_json e))]
      | `ValidationException e ->
          `Assoc
            [("error", (`String "ValidationException"));
            ("details", (ValidationException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("Status", (Option.map x.status ~f:Status.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let status =
        (Option.map ~f:Status.of_xml) (Xml.child xml_arg0 "Status") in
      make ?status ()
    let of_json json =
      let status = field_map json "Status" Status.of_json in make ?status ()
    let to_json v = composed_to_json to_value v
  end
