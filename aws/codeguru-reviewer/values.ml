          `ValidationException (ValidationException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "AccessDeniedException" ->
          `AccessDeniedException (AccessDeniedException.of_xml xml)
      | "ConflictException" ->
          `ConflictException (ConflictException.of_xml xml)
      | "InternalServerException" ->
          `InternalServerException (InternalServerException.of_xml xml)
      | "ThrottlingException" ->
          `ThrottlingException (ThrottlingException.of_xml xml)
      | "ValidationException" ->
          `ValidationException (ValidationException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `AccessDeniedException e ->
          `Assoc
            [("error", (`String "AccessDeniedException"));
            ("details", (AccessDeniedException.to_json e))]
      | `ConflictException e ->
          `Assoc
            [("error", (`String "ConflictException"));
            ("details", (ConflictException.to_json e))]
      | `InternalServerException e ->
          `Assoc
            [("error", (`String "InternalServerException"));
            ("details", (InternalServerException.to_json e))]
      | `ThrottlingException e ->
          `Assoc
            [("error", (`String "ThrottlingException"));
            ("details", (ThrottlingException.to_json e))]
      | `ValidationException e ->
          `Assoc
            [("error", (`String "ValidationException"));
            ("details", (ValidationException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("RepositoryAssociation",
           (Option.map x.repositoryAssociation
              ~f:RepositoryAssociation.to_value));
        ("Tags", (Option.map x.tags ~f:TagMap.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let tags = (Option.map ~f:TagMap.of_xml) (Xml.child xml_arg0 "Tags") in
      let repositoryAssociation =
        (Option.map ~f:RepositoryAssociation.of_xml)
          (Xml.child xml_arg0 "RepositoryAssociation") in
      make ?tags ?repositoryAssociation ()
    let of_json json =
      let tags = field_map json "Tags" TagMap.of_json in
      let repositoryAssociation =
        field_map json "RepositoryAssociation" RepositoryAssociation.of_json in
      make ?tags ?repositoryAssociation ()
    let to_json v = composed_to_json to_value v
  end
module AssociateRepositoryRequest =
  struct
    type nonrec t =
      {
      repository: Repository.t ;
      clientRequestToken: ClientRequestToken.t option ;
      tags: TagMap.t option ;
      kMSKeyDetails: KMSKeyDetails.t option }
    let context_ = "AssociateRepositoryRequest"
    let make ?clientRequestToken =
      fun ?tags ->
        fun ?kMSKeyDetails ->
          fun ~repository ->
            fun () -> { clientRequestToken; tags; kMSKeyDetails; repository }
    let to_value x =
      structure_to_value
        [("Repository", (Some (Repository.to_value x.repository)));
        ("ClientRequestToken",
          (Option.map x.clientRequestToken ~f:ClientRequestToken.to_value));
        ("Tags", (Option.map x.tags ~f:TagMap.to_value));
        ("KMSKeyDetails",
          (Option.map x.kMSKeyDetails ~f:KMSKeyDetails.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let kMSKeyDetails =
        (Option.map ~f:KMSKeyDetails.of_xml)
          (Xml.child xml_arg0 "KMSKeyDetails") in
      let tags = (Option.map ~f:TagMap.of_xml) (Xml.child xml_arg0 "Tags") in
      let clientRequestToken =
        (Option.map ~f:ClientRequestToken.of_xml)
          (Xml.child xml_arg0 "ClientRequestToken") in
      let repository =
        Repository.of_xml
          (Xml.child_exn ~context:context_ xml_arg0 "Repository") in
      make ?kMSKeyDetails ?tags ?clientRequestToken ~repository ()
    let of_json json =
      let kMSKeyDetails =
        field_map json "KMSKeyDetails" KMSKeyDetails.of_json in
      let tags = field_map json "Tags" TagMap.of_json in
      let clientRequestToken =
        field_map json "ClientRequestToken" ClientRequestToken.of_json in
      let repository = field_map_exn json "Repository" Repository.of_json in
      make ?kMSKeyDetails ?tags ?clientRequestToken ~repository ()
    let to_json v = composed_to_json to_value v
  end