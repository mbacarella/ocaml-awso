      let datastoreName =
        (Option.map ~f:DatastoreName.of_xml)
          (Xml.child xml_arg0 "DatastoreName") in
      make ?tags ?clientToken ?preloadDataConfig ?sseConfiguration
        ~datastoreTypeVersion ?datastoreName ()
