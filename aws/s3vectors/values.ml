      function
      | `AccessDeniedException e ->
          `Assoc
            [("error", (`String "AccessDeniedException"));
            ("details", (AccessDeniedException.to_json e))]
      | `ConflictException e ->
          `Assoc
            [("error", (`String "ConflictException"));
            ("details", (ConflictException.to_json e))]
      | `InternalServerException e ->
          `Assoc
            [("error", (`String "InternalServerException"));
            ("details", (InternalServerException.to_json e))]
      | `NotFoundException e ->
          `Assoc
            [("error", (`String "NotFoundException"));
            ("details", (NotFoundException.to_json e))]
      | `RequestTimeoutException e ->
          `Assoc
            [("error", (`String "RequestTimeoutException"));
            ("details", (RequestTimeoutException.to_json e))]
      | `ServiceUnavailableException e ->
          `Assoc
            [("error", (`String "ServiceUnavailableException"));
            ("details", (ServiceUnavailableException.to_json e))]
      | `TooManyRequestsException e ->
          `Assoc
