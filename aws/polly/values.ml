      lexiconArn: LexiconArn.t option
        [@ocaml.doc "Amazon Resource Name (ARN) of the lexicon."];
      lexemesCount: LexemesCount.t option
        [@ocaml.doc "Number of lexemes in the lexicon."];
      size: Size.t option
        [@ocaml.doc "Total size of the lexicon, in characters."]}
    let make ?alphabet =
      fun ?languageCode ->
        fun ?lastModified ->
          fun ?lexiconArn ->
            fun ?lexemesCount ->
              fun ?size ->
                fun () ->
                  {
                    alphabet;
                    languageCode;
                    lastModified;
                    lexiconArn;
                    lexemesCount;
                    size
                  }
    let to_value x =
      structure_to_value
        [("Alphabet", (Option.map x.alphabet ~f:Alphabet.to_value));
        ("LanguageCode",
          (Option.map x.languageCode ~f:LanguageCode.to_value));
        ("LastModified",
          (Option.map x.lastModified ~f:LastModified.to_value));
        ("LexiconArn", (Option.map x.lexiconArn ~f:LexiconArn.to_value));
        ("LexemesCount",
          (Option.map x.lexemesCount ~f:LexemesCount.to_value));
        ("Size", (Option.map x.size ~f:Size.to_value))]
