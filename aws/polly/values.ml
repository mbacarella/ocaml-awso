module SynthesizeSpeechOutput =
  struct
    type nonrec t =
      {
      audioStream: AudioStream.t option
        [@ocaml.doc "Stream containing the synthesized speech."];
      contentType: ContentType.t option
        [@ocaml.doc
          "Specifies the type audio stream. This should reflect the OutputFormat parameter in your request. If you request mp3 as the OutputFormat, the ContentType returned is audio/mpeg. If you request ogg_vorbis as the OutputFormat, the ContentType returned is audio/ogg. If you request pcm as the OutputFormat, the ContentType returned is audio/pcm in a signed 16-bit, 1 channel (mono), little-endian format. If you request json as the OutputFormat, the ContentType returned is application/x-json-stream."];
      requestCharacters: RequestCharacters.t option
        [@ocaml.doc "Number of characters synthesized."]}
    type nonrec error =
      [ `EngineNotSupportedException of EngineNotSupportedException.t 
      | `InvalidSampleRateException of InvalidSampleRateException.t 
      | `InvalidSsmlException of InvalidSsmlException.t 
      | `LanguageNotSupportedException of LanguageNotSupportedException.t 
      | `LexiconNotFoundException of LexiconNotFoundException.t 
      | `MarksNotSupportedForFormatException of
          MarksNotSupportedForFormatException.t 
      | `ServiceFailureException of ServiceFailureException.t 
      | `SsmlMarksNotSupportedForTextTypeException of
          SsmlMarksNotSupportedForTextTypeException.t 
      | `TextLengthExceededException of TextLengthExceededException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?audioStream =
      fun ?contentType ->
        fun ?requestCharacters ->
          fun () -> { audioStream; contentType; requestCharacters }
    let error_of_json name json =
      match name with
      | "EngineNotSupportedException" ->
          `EngineNotSupportedException
            (EngineNotSupportedException.of_json json)
      | "InvalidSampleRateException" ->
          `InvalidSampleRateException
            (InvalidSampleRateException.of_json json)
      | "InvalidSsmlException" ->
          `InvalidSsmlException (InvalidSsmlException.of_json json)
      | "LanguageNotSupportedException" ->
          `LanguageNotSupportedException
            (LanguageNotSupportedException.of_json json)
      | "LexiconNotFoundException" ->
          `LexiconNotFoundException (LexiconNotFoundException.of_json json)
      | "MarksNotSupportedForFormatException" ->
          `MarksNotSupportedForFormatException
            (MarksNotSupportedForFormatException.of_json json)
      | "ServiceFailureException" ->
          `ServiceFailureException (ServiceFailureException.of_json json)
      | "SsmlMarksNotSupportedForTextTypeException" ->
          `SsmlMarksNotSupportedForTextTypeException
            (SsmlMarksNotSupportedForTextTypeException.of_json json)
      | "TextLengthExceededException" ->
          `TextLengthExceededException
            (TextLengthExceededException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "EngineNotSupportedException" ->
          `EngineNotSupportedException
            (EngineNotSupportedException.of_xml xml)
      | "InvalidSampleRateException" ->
