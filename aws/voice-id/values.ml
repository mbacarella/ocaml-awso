  struct
    type nonrec t =
      {
      domainId: DomainId.t
        [@ocaml.doc
          "The identifier of the domain that contains the fraudster."];
      fraudsterId: FraudsterId.t
        [@ocaml.doc
          "The identifier of the fraudster to be associated with the watchlist."];
