      structure_to_value
        [("DomainId", (Some (DomainId.to_value x.domainId)));
        ("FraudsterId", (Some (FraudsterId.to_value x.fraudsterId)));
        ("WatchlistId", (Some (WatchlistId.to_value x.watchlistId)))]
    let to_query v = to_query to_value v
