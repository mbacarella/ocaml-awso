module CreateAppResponse =
  struct
    type nonrec t =
      {
      appSummary: AppSummary.t option ;
      serverGroups: ServerGroups.t option ;
      tags: Tags.t option }
    type nonrec error =
      [ `InternalError of InternalError.t 
      | `InvalidParameterException of InvalidParameterException.t 
      | `MissingRequiredParameterException of
          MissingRequiredParameterException.t 
      | `OperationNotPermittedException of OperationNotPermittedException.t 
      | `UnauthorizedOperationException of UnauthorizedOperationException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?appSummary =
      fun ?serverGroups ->
        fun ?tags -> fun () -> { appSummary; serverGroups; tags }
    let error_of_json name json =
      match name with
      | "InternalError" -> `InternalError (InternalError.of_json json)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_json json)
      | "MissingRequiredParameterException" ->
          `MissingRequiredParameterException
            (MissingRequiredParameterException.of_json json)
      | "OperationNotPermittedException" ->
          `OperationNotPermittedException
            (OperationNotPermittedException.of_json json)
      | "UnauthorizedOperationException" ->
          `UnauthorizedOperationException
            (UnauthorizedOperationException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "InternalError" -> `InternalError (InternalError.of_xml xml)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_xml xml)
      | "MissingRequiredParameterException" ->
          `MissingRequiredParameterException
            (MissingRequiredParameterException.of_xml xml)
      | "OperationNotPermittedException" ->
          `OperationNotPermittedException
            (OperationNotPermittedException.of_xml xml)
      | "UnauthorizedOperationException" ->
          `UnauthorizedOperationException
            (UnauthorizedOperationException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `InternalError e ->
          `Assoc
            [("error", (`String "InternalError"));
            ("details", (InternalError.to_json e))]
      | `InvalidParameterException e ->
          `Assoc
            [("error", (`String "InvalidParameterException"));
            ("details", (InvalidParameterException.to_json e))]
      | `MissingRequiredParameterException e ->
          `Assoc
            [("error", (`String "MissingRequiredParameterException"));
            ("details", (MissingRequiredParameterException.to_json e))]
      | `OperationNotPermittedException e ->
          `Assoc
            [("error", (`String "OperationNotPermittedException"));
            ("details", (OperationNotPermittedException.to_json e))]
      | `UnauthorizedOperationException e ->
          `Assoc
            [("error", (`String "UnauthorizedOperationException"));
            ("details", (UnauthorizedOperationException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("appSummary", (Option.map x.appSummary ~f:AppSummary.to_value));
        ("serverGroups",
          (Option.map x.serverGroups ~f:ServerGroups.to_value));
        ("tags", (Option.map x.tags ~f:Tags.to_value))]
