  let handle_error err error_of_json =
    let generic_error () = Error (`Transport err) in
    match err with
    | `Too_many_redirects -> generic_error ()
    | `Bad_response
        { Awso.Http.Io.Error.code = code; body; x_amzn_error_type = _ } ->
