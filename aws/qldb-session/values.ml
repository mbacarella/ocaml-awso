  struct
    type nonrec t =
      {
      startSession: StartSessionResult.t option ;
      startTransaction: StartTransactionResult.t option ;
      endSession: EndSessionResult.t option ;
      commitTransaction: CommitTransactionResult.t option ;
      abortTransaction: AbortTransactionResult.t option ;
      executeStatement: ExecuteStatementResult.t option ;
      fetchPage: FetchPageResult.t option }
    type nonrec error =
      [ `BadRequestException of BadRequestException.t 
      | `CapacityExceededException of CapacityExceededException.t 
      | `InvalidSessionException of InvalidSessionException.t 
      | `LimitExceededException of LimitExceededException.t 
      | `OccConflictException of OccConflictException.t 
      | `RateExceededException of RateExceededException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?startSession =
      fun ?startTransaction ->
        fun ?endSession ->
          fun ?commitTransaction ->
            fun ?abortTransaction ->
              fun ?executeStatement ->
                fun ?fetchPage ->
                  fun () ->
                    {
                      startSession;
                      startTransaction;
                      endSession;
                      commitTransaction;
                      abortTransaction;
                      executeStatement;
                      fetchPage
                    }
    let error_of_json name json =
      match name with
      | "BadRequestException" ->
          `BadRequestException (BadRequestException.of_json json)
      | "CapacityExceededException" ->
          `CapacityExceededException (CapacityExceededException.of_json json)
      | "InvalidSessionException" ->
          `InvalidSessionException (InvalidSessionException.of_json json)
      | "LimitExceededException" ->
          `LimitExceededException (LimitExceededException.of_json json)
      | "OccConflictException" ->
          `OccConflictException (OccConflictException.of_json json)
      | "RateExceededException" ->
          `RateExceededException (RateExceededException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "BadRequestException" ->
          `BadRequestException (BadRequestException.of_xml xml)
      | "CapacityExceededException" ->
          `CapacityExceededException (CapacityExceededException.of_xml xml)
      | "InvalidSessionException" ->
          `InvalidSessionException (InvalidSessionException.of_xml xml)
      | "LimitExceededException" ->
          `LimitExceededException (LimitExceededException.of_xml xml)
      | "OccConflictException" ->
          `OccConflictException (OccConflictException.of_xml xml)
      | "RateExceededException" ->
          `RateExceededException (RateExceededException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `BadRequestException e ->
          `Assoc
            [("error", (`String "BadRequestException"));
            ("details", (BadRequestException.to_json e))]
      | `CapacityExceededException e ->
          `Assoc
            [("error", (`String "CapacityExceededException"));
            ("details", (CapacityExceededException.to_json e))]
      | `InvalidSessionException e ->
          `Assoc
            [("error", (`String "InvalidSessionException"));
            ("details", (InvalidSessionException.to_json e))]
      | `LimitExceededException e ->
          `Assoc
            [("error", (`String "LimitExceededException"));
            ("details", (LimitExceededException.to_json e))]
      | `OccConflictException e ->
          `Assoc
            [("error", (`String "OccConflictException"));
            ("details", (OccConflictException.to_json e))]
      | `RateExceededException e ->
          `Assoc
            [("error", (`String "RateExceededException"));
            ("details", (RateExceededException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
