module UpdateLinkInput =
  struct
    type nonrec t =
      {
      identifier: ResourceIdentifier.t
        [@ocaml.doc "The ARN of the link that you want to update."];
      includeTags: IncludeTags.t option
        [@ocaml.doc
          "Specifies whether to include the tags associated with the link in the response after the update operation. When IncludeTags is set to true and the caller has the required permission, oam:ListTagsForResource, the API will return the tags for the specified resource. If the caller doesn't have the required permission, oam:ListTagsForResource, the API will raise an exception. The default value is false."];
      linkConfiguration: LinkConfiguration.t option
        [@ocaml.doc
          "Use this structure to filter which metric namespaces and which log groups are to be shared from the source account to the monitoring account."];
      resourceTypes: ResourceTypesInput.t
        [@ocaml.doc
          "An array of strings that define which types of data that the source account will send to the monitoring account. Your input here replaces the current set of data types that are shared."]}
    let context_ = "UpdateLinkInput"
    let make ?includeTags =
      fun ?linkConfiguration ->
        fun ~identifier ->
          fun ~resourceTypes ->
            fun () ->
              { includeTags; linkConfiguration; identifier; resourceTypes }
    let to_value x =
      structure_to_value
        [("Identifier", (Some (ResourceIdentifier.to_value x.identifier)));
        ("IncludeTags", (Option.map x.includeTags ~f:IncludeTags.to_value));
        ("LinkConfiguration",
          (Option.map x.linkConfiguration ~f:LinkConfiguration.to_value));
        ("ResourceTypes",
          (Some (ResourceTypesInput.to_value x.resourceTypes)))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let resourceTypes =
        ResourceTypesInput.of_xml
          (Xml.child_exn ~context:context_ xml_arg0 "ResourceTypes") in
      let linkConfiguration =
        (Option.map ~f:LinkConfiguration.of_xml)
          (Xml.child xml_arg0 "LinkConfiguration") in
      let includeTags =
        (Option.map ~f:IncludeTags.of_xml) (Xml.child xml_arg0 "IncludeTags") in
      let identifier =
        ResourceIdentifier.of_xml
          (Xml.child_exn ~context:context_ xml_arg0 "Identifier") in
      make ~resourceTypes ?linkConfiguration ?includeTags ~identifier ()
    let of_string s = of_xml (Awso.Xml.parse_response s)[@@warning "-32"]
    let of_json json__ =
      let resourceTypes =
        field_map_exn json__ "ResourceTypes" ResourceTypesInput.of_json in
      let linkConfiguration =
        field_map json__ "LinkConfiguration" LinkConfiguration.of_json in
      let includeTags = field_map json__ "IncludeTags" IncludeTags.of_json in
      let identifier =
        field_map_exn json__ "Identifier" ResourceIdentifier.of_json in
      make ~resourceTypes ?linkConfiguration ?includeTags ~identifier ()
