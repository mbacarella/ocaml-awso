module UpdateLinkOutput =
  struct
    type nonrec t =
      {
      arn: String_.t option
        [@ocaml.doc "The ARN of the link that you have updated."];
      id: String_.t option
        [@ocaml.doc
          "The random ID string that Amazon Web Services generated as part of the sink ARN."];
      label: String_.t option
        [@ocaml.doc
          "The label assigned to this link, with the variables resolved to their actual values."];
      labelTemplate: LabelTemplate.t option
        [@ocaml.doc
          "The exact label template that was specified when the link was created, with the template variables not resolved."];
      linkConfiguration: LinkConfiguration.t option
        [@ocaml.doc
          "This structure includes filters that specify which metric namespaces and which log groups are shared from the source account to the monitoring account."];
      resourceTypes: ResourceTypesOutput.t option
        [@ocaml.doc "The resource types now supported by this link."];
      sinkArn: String_.t option
        [@ocaml.doc "The ARN of the sink that is used for this link."];
      tags: TagMapOutput.t option
        [@ocaml.doc "The tags assigned to the link."]}
    type nonrec error =
      [ `InternalServiceFault of InternalServiceFault.t 
      | `InvalidParameterException of InvalidParameterException.t 
      | `MissingRequiredParameterException of
          MissingRequiredParameterException.t 
      | `ResourceNotFoundException of ResourceNotFoundException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?arn =
      fun ?id ->
        fun ?label ->
          fun ?labelTemplate ->
            fun ?linkConfiguration ->
              fun ?resourceTypes ->
                fun ?sinkArn ->
                  fun ?tags ->
                    fun () ->
                      {
                        arn;
                        id;
                        label;
                        labelTemplate;
                        linkConfiguration;
                        resourceTypes;
                        sinkArn;
                        tags
                      }
    let error_of_json name json =
      match name with
      | "InternalServiceFault" ->
          `InternalServiceFault (InternalServiceFault.of_json json)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_json json)
      | "MissingRequiredParameterException" ->
          `MissingRequiredParameterException
            (MissingRequiredParameterException.of_json json)
      | "ResourceNotFoundException" ->
          `ResourceNotFoundException (ResourceNotFoundException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "InternalServiceFault" ->
          `InternalServiceFault (InternalServiceFault.of_xml xml)
      | "InvalidParameterException" ->
          `InvalidParameterException (InvalidParameterException.of_xml xml)
      | "MissingRequiredParameterException" ->
          `MissingRequiredParameterException
            (MissingRequiredParameterException.of_xml xml)
      | "ResourceNotFoundException" ->
          `ResourceNotFoundException (ResourceNotFoundException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `InternalServiceFault e ->
          `Assoc
            [("error", (`String "InternalServiceFault"));
            ("details", (InternalServiceFault.to_json e))]
      | `InvalidParameterException e ->
          `Assoc
            [("error", (`String "InvalidParameterException"));
            ("details", (InvalidParameterException.to_json e))]
      | `MissingRequiredParameterException e ->
          `Assoc
            [("error", (`String "MissingRequiredParameterException"));
            ("details", (MissingRequiredParameterException.to_json e))]
      | `ResourceNotFoundException e ->
          `Assoc
            [("error", (`String "ResourceNotFoundException"));
            ("details", (ResourceNotFoundException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("Arn", (Option.map x.arn ~f:String_.to_value));
        ("Id", (Option.map x.id ~f:String_.to_value));
