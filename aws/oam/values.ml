      let label = field_map json__ "Label" String_.of_json in
      let id = field_map json__ "Id" String_.of_json in
      let arn = field_map json__ "Arn" String_.of_json in
      make ?tags ?sinkArn ?resourceTypes ?linkConfiguration ?labelTemplate
