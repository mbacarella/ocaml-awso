          (Xml.child xml_arg0 "LinkConfiguration") in
      let labelTemplate =
        (Option.map ~f:LabelTemplate.of_xml)
          (Xml.child xml_arg0 "LabelTemplate") in
      let label = (Option.map ~f:String_.of_xml) (Xml.child xml_arg0 "Label") in
      let id = (Option.map ~f:String_.of_xml) (Xml.child xml_arg0 "Id") in
      let arn = (Option.map ~f:String_.of_xml) (Xml.child xml_arg0 "Arn") in
      make ?tags ?sinkArn ?resourceTypes ?linkConfiguration ?labelTemplate
        ?label ?id ?arn ()
    let of_string s = of_xml (Awso.Xml.parse_response s)[@@warning "-32"]
    let of_json json__ =
      let tags = field_map json__ "Tags" TagMapOutput.of_json in
      let sinkArn = field_map json__ "SinkArn" String_.of_json in
      let resourceTypes =
        field_map json__ "ResourceTypes" ResourceTypesOutput.of_json in
