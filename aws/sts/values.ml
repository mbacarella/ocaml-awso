      function
      | `ExpiredTokenException e ->
          `Assoc
            [("error", (`String "ExpiredTokenException"));
            ("details", (ExpiredTokenException.to_json e))]
      | `MalformedPolicyDocumentException e ->
          `Assoc
            [("error", (`String "MalformedPolicyDocumentException"));
