    ([%map_open.Command
       let cli_profile =
         flag "-cli-profile" (optional string) ~doc:"NAME aws profile to use"
       and cli_region =
         flag "-cli-region" (optional string) ~doc:"REGION override region"
       and endpoint_url =
         flag "-endpoint-url" (optional string)
           ~doc:"URL override endpoint url"
       and newComponentName =
         flag "new-component-name" (optional string)
           ~doc:"STRING CustomComponentName"
       and resourceList =
         flag "resource-list" (optional json_arg) ~doc:"JSON ResourceList"
       and resourceGroupName =
         flag "resource-group-name" (required string)
           ~doc:"STRING ResourceGroupName"
       and componentName =
         flag "component-name" (required string)
           ~doc:"STRING CustomComponentName" in
       fun () ->
         call ?endpoint_url ?profile:cli_profile ?region:cli_region
           Io.update_component
           (Values.UpdateComponentRequest.make ?newComponentName
              ?resourceList:(Option.map ~f:Values.ResourceList.of_json
                               resourceList) ~resourceGroupName
              ~componentName ())
           (Some Values.UpdateComponentResponse.to_json)
           (Some Values.UpdateComponentResponse.error_to_json)])
