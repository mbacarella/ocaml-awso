      let applicationInfo =
        (Option.map ~f:ApplicationInfo.of_xml)
          (Xml.child xml_arg0 "ApplicationInfo") in
      make ?applicationInfo ()
    let of_json json =
      let applicationInfo =
        field_map json "ApplicationInfo" ApplicationInfo.of_json in
      make ?applicationInfo ()
