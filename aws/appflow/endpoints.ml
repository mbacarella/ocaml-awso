  (resp : (Awso.Http.Response.t, Awso.Http.Io.Error.call) result) :
  (o, [ `AWS of e  | `Transport of Awso.Http.Io.Error.call ]) result=
  let handle_error err error_of_json =
    match err with
    | `Too_many_redirects -> Error (`Transport `Too_many_redirects)
    | `Bad_response
        { Awso.Http.Io.Error.code = code; body; x_amzn_error_type } ->
        let generic_error () =
          Error
            (`Transport
               (`Bad_response
                  { Awso.Http.Io.Error.code = code; body; x_amzn_error_type })) in
        (match (x_amzn_error_type, error_of_json,
                 ((code >= 400) && (code <= 599)))
         with
         | (Some error_type, Some error_of_json, true) ->
             let json = Yojson.Safe.from_string body in
             Error (`AWS (error_of_json error_type json))
         | (None, Some error_of_json, true) ->
             (try
                let json = Yojson.Safe.from_string body in
                match json |> (Yojson.Safe.Util.member "__type") with
                | `String error_type ->
                    let error_type =
                      match String.lsplit2 error_type ~on:'#' with
                      | Some (_, s) -> s
                      | None -> error_type in
                    Error (`AWS (error_of_json error_type json))
                | `Null -> generic_error ()
                | _ ->
                    failwithf "Error '__type' did not have string type: %s"
                      body ()
              with | _ -> generic_error ())
         | (None, _, _) | (_, None, _) | (_, _, false) -> generic_error ()) in
  let response_to_json resp =
    Yojson.Safe.from_string (Awso.Http.Response.body resp) in
  let _ = resp in
  let _ = handle_error in
  let _ = response_to_json in
  match endpoint with
  | CreateConnectorProfile ->
      (match resp with
       | Error err ->
           handle_error err
             (Some CreateConnectorProfileResponse.error_of_json)
       | Ok resp ->
           Ok
             (CreateConnectorProfileResponse.of_json (response_to_json resp)))
  | CreateFlow ->
      (match resp with
       | Error err ->
           handle_error err (Some CreateFlowResponse.error_of_json)
       | Ok resp -> Ok (CreateFlowResponse.of_json (response_to_json resp)))
  | DeleteConnectorProfile ->
      (match resp with
       | Error err ->
           handle_error err
             (Some DeleteConnectorProfileResponse.error_of_json)
       | Ok resp ->
           let headers =
             Awso.Http.Headers.to_list (Awso.Http.Response.headers resp) in
           Ok
             (DeleteConnectorProfileResponse.of_header_and_body (headers, ())))
  | DeleteFlow ->
