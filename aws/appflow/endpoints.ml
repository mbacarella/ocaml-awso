  (resp : Awso.Http.Response.t) : (o, e) result=
  let code = Awso.Http.Status.to_code (Awso.Http.Response.status resp) in
  let is_success = (code >= 200) && (code < 300) in
  let x_amzn_error_type =
    let headers = Awso.Http.Headers.to_list (Awso.Http.Response.headers resp) in
    match List.Assoc.find ~equal:String.Caseless.equal headers
            "x-amzn-ErrorType"
    with
    | None -> None
    | Some value ->
        (match String.lsplit2 value ~on:':' with
         | None -> Some value
         | Some (v, _) -> Some v) in
  let parse_aws_error error_of_json =
    let body = Awso.Http.Response.body resp in
    let bail () =
      raise
        (Awso.Http.Io.Error.Bad_response
           { Awso.Http.Io.Error.code = code; body; x_amzn_error_type }) in
    match (x_amzn_error_type, error_of_json,
            ((code >= 400) && (code <= 599)))
    with
    | (Some error_type, Some error_of_json, true) ->
        let json = Yojson.Safe.from_string body in
        error_of_json error_type json
    | (None, Some error_of_json, true) ->
        (try
           let json = Yojson.Safe.from_string body in
           match json |> (Yojson.Safe.Util.member "__type") with
           | `String error_type ->
               let error_type =
                 match String.lsplit2 error_type ~on:'#' with
                 | Some (_, s) -> s
                 | None -> error_type in
               error_of_json error_type json
           | `Null -> bail ()
           | _ ->
               failwithf "Error '__type' did not have string type: %s" body
                 ()
         with | _ -> bail ())
    | (None, _, _) | (_, None, _) | (_, _, false) -> bail () in
  let response_to_json resp =
    Yojson.Safe.from_string (Awso.Http.Response.body resp) in
  let _ = parse_aws_error in
  let _ = response_to_json in
  let _ = resp in
  match endpoint with
  | CreateConnectorProfile ->
      if is_success
      then
        Ok (CreateConnectorProfileResponse.of_json (response_to_json resp))
      else
        Error
          (parse_aws_error
             (Some CreateConnectorProfileResponse.error_of_json))
  | CreateFlow ->
      if is_success
      then Ok (CreateFlowResponse.of_json (response_to_json resp))
      else Error (parse_aws_error (Some CreateFlowResponse.error_of_json))
  | DeleteConnectorProfile ->
      if is_success
      then
        let headers =
          Awso.Http.Headers.to_list (Awso.Http.Response.headers resp) in
        Ok (DeleteConnectorProfileResponse.of_header_and_body (headers, ()))
      else
        Error
          (parse_aws_error
             (Some DeleteConnectorProfileResponse.error_of_json))
  | DeleteFlow ->
      if is_success
      then
        let headers =
          Awso.Http.Headers.to_list (Awso.Http.Response.headers resp) in
        Ok (DeleteFlowResponse.of_header_and_body (headers, ()))
      else Error (parse_aws_error (Some DeleteFlowResponse.error_of_json))
  | DescribeConnector ->
      if is_success
      then Ok (DescribeConnectorResponse.of_json (response_to_json resp))
      else
        Error
          (parse_aws_error (Some DescribeConnectorResponse.error_of_json))
  | DescribeConnectorEntity ->
      if is_success
      then
        Ok (DescribeConnectorEntityResponse.of_json (response_to_json resp))
      else
