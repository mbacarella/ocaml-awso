  struct
    type nonrec t =
      {
      jobId: DescribeJobExecutionJobId.t ;
      thingName: ThingName.t ;
      includeJobDocument: IncludeJobDocument.t option ;
      executionNumber: ExecutionNumber.t option }
    let context_ = "DescribeJobExecutionRequest"
    let make ?includeJobDocument =
      fun ?executionNumber ->
        fun ~jobId ->
          fun ~thingName ->
            fun () ->
              { includeJobDocument; executionNumber; jobId; thingName }
    let to_value x =
      structure_to_value
        [("jobId", (Some (DescribeJobExecutionJobId.to_value x.jobId)));
        ("thingName", (Some (ThingName.to_value x.thingName)));
        ("includeJobDocument",
          (Option.map x.includeJobDocument ~f:IncludeJobDocument.to_value));
        ("executionNumber",
          (Option.map x.executionNumber ~f:ExecutionNumber.to_value))]
    let to_query v = to_query to_value v
