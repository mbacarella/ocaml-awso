module BackupPolicyDescription =
  struct
    type nonrec t =
      {
      backupPolicy: BackupPolicy.t option
        [@ocaml.doc
          "Describes the file system's backup policy, indicating whether automatic backups are turned on or off."]}
    type nonrec error =
      [ `BadRequest of BadRequest.t 
      | `FileSystemNotFound of FileSystemNotFound.t 
      | `InternalServerError of InternalServerError.t 
      | `PolicyNotFound of PolicyNotFound.t 
      | `ValidationException of ValidationException.t 
      | `Unknown_operation_error of (string * string option) ]
    let make ?backupPolicy = fun () -> { backupPolicy }
    let error_of_json name json =
      match name with
      | "BadRequest" -> `BadRequest (BadRequest.of_json json)
      | "FileSystemNotFound" ->
          `FileSystemNotFound (FileSystemNotFound.of_json json)
      | "InternalServerError" ->
          `InternalServerError (InternalServerError.of_json json)
      | "PolicyNotFound" -> `PolicyNotFound (PolicyNotFound.of_json json)
      | "ValidationException" ->
          `ValidationException (ValidationException.of_json json)
      | name ->
          `Unknown_operation_error
            (name, (Some (Yojson.Safe.to_string json)))
    let error_of_xml name xml =
      match name with
      | "BadRequest" -> `BadRequest (BadRequest.of_xml xml)
      | "FileSystemNotFound" ->
          `FileSystemNotFound (FileSystemNotFound.of_xml xml)
      | "InternalServerError" ->
          `InternalServerError (InternalServerError.of_xml xml)
      | "PolicyNotFound" -> `PolicyNotFound (PolicyNotFound.of_xml xml)
      | "ValidationException" ->
          `ValidationException (ValidationException.of_xml xml)
      | name ->
          `Unknown_operation_error (name, (Some (Awso.Xml.to_string xml)))
    let error_to_json : error -> Yojson.Safe.t =
      function
      | `BadRequest e ->
          `Assoc
            [("error", (`String "BadRequest"));
            ("details", (BadRequest.to_json e))]
      | `FileSystemNotFound e ->
          `Assoc
            [("error", (`String "FileSystemNotFound"));
            ("details", (FileSystemNotFound.to_json e))]
      | `InternalServerError e ->
          `Assoc
            [("error", (`String "InternalServerError"));
            ("details", (InternalServerError.to_json e))]
      | `PolicyNotFound e ->
          `Assoc
            [("error", (`String "PolicyNotFound"));
            ("details", (PolicyNotFound.to_json e))]
      | `ValidationException e ->
          `Assoc
            [("error", (`String "ValidationException"));
            ("details", (ValidationException.to_json e))]
      | `Unknown_operation_error (code, msg) ->
          `Assoc (("error", (`String code)) ::
            ((match msg with
              | None -> []
              | Some m -> [("message", (`String m))])))
    let to_value x =
      structure_to_value
        [("BackupPolicy",
           (Option.map x.backupPolicy ~f:BackupPolicy.to_value))]
    let to_query v = to_query to_value v
    let of_xml xml_arg0 =
      let backupPolicy =
        (Option.map ~f:BackupPolicy.of_xml)
          (Xml.child xml_arg0 "BackupPolicy") in
      make ?backupPolicy ()
    let of_json json =
      let backupPolicy = field_map json "BackupPolicy" BackupPolicy.of_json in
      make ?backupPolicy ()
    let to_json v = composed_to_json to_value v
  end[@@ocaml.doc
       "Returns the backup policy for the specified EFS file system."]