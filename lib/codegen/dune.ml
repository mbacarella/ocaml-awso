open! Import

let dashes_to_underscores : string -> string =
  String.map ~f:(function
    | '-' -> '_'
    | c -> c)
;;

let library_base ~service =
  let extra_deps =
    match service with
    | "s3" -> []
    | _ -> []
  in
  let service_unders = dashes_to_underscores service in
  let extra_deps = String.concat ~sep:" " extra_deps in
  let extra_ppx =
    match service with
    | "cognito-idp" | "s3" -> " ppx_yojson_conv"
    | _ -> ""
  in
  sprintf
    {|
(library
  (name awso_%s)
  (public_name awso.%s)
  (libraries awso %s)
  (ocamlopt_flags (-linscan))
  (preprocess (pps ppx_expect%s))
  (synopsis "OCaml AWS %s API.")
  (inline_tests))
     |}
    service_unders
    service
    extra_deps
    extra_ppx
    service
;;

let num_value_submodules = function
  | "ec2" -> 8
  | "ssm" | "iot" | "rds" | "sagemaker" -> 2
  | _ -> 0
;;

let make ~service =
  sprintf ";; do not edit! generated by awso-codegen\n%s" (library_base ~service)
;;

let has_addendum ~io_kind ~service =
  match io_kind with
  | `Async ->
    List.mem
      ~equal:String.equal
      [ "cognito-identity"; "cognito-idp"; "glue"; "s3"; "sqs"; "sso"; "sts" ]
      service
  | `Lwt -> false
;;

let needs_yojson_conv ~io_kind ~service =
  match io_kind with
  | `Async -> List.mem ~equal:String.equal [ "glue"; "s3"; "sqs"; "sts" ] service
  | `Lwt -> false
;;

let library_io (io_kind : [ `Async | `Lwt ]) ~service =
  let io =
    match io_kind with
    | `Async -> "async"
    | `Lwt -> "lwt"
  in
  let service_under = dashes_to_underscores service in
  let preprocess =
    match io_kind with
    | `Async ->
      let ppx_yojson =
        if needs_yojson_conv ~io_kind ~service then " ppx_yojson_conv" else ""
      in
      sprintf "\n  (preprocess (pps ppx_jane%s))\n  (inline_tests)" ppx_yojson
    | `Lwt -> ""
  in
  sprintf
    {|
(library
  (name awso_%s_%s)
  (public_name awso-%s.%s)
  (libraries awso.%s awso-%s)%s)
    |}
    service_under
    io
    io
    service
    service
    io
    preprocess
;;

let num_cli_submodules = function
  | "ec2" -> 3
  | "iot" | "rds" | "sagemaker" -> 2
  | _ -> 0
;;

let make_io (io_kind : [ `Async | `Lwt ]) ~service =
  sprintf ";; do not edit! generated by awso-codegen!\n%s" (library_io io_kind ~service)
;;

let make_cli_async ~service =
  let service_ = dashes_to_underscores service in
  sprintf
    {|;; do not edit! generated by awso-codegen!
(executable
  (package awso-async)
  (name awso_%s)
  (public_name awso-%s)
  (libraries awso-async.%s awso-async core_unix.command_unix)
  (preprocess (pps ppx_jane)))
|}
    service_
    service
    service
;;

let make_awso_cli_dune ~services =
  let libs =
    services |> List.map ~f:(sprintf "awso-async.%s") |> String.concat ~sep:" "
  in
  sprintf
    {|(executable
  (package awso-cli)
  (name awso_main)
  (public_name awso)
  (libraries awso-async core_unix.command_unix %s)
  (preprocess (pps ppx_jane)))
|}
    libs
;;

let make_awso_cli_main ~services =
  let entries =
    services
    |> List.mapi ~f:(fun i service ->
      let sep = if i = 0 then "[ " else "; " in
      sprintf
        {|    %s"%s", Awso_%s_async.Cli.main|}
        sep
        service
        (dashes_to_underscores service))
    |> String.concat ~sep:"\n"
  in
  sprintf
    {|open! Core

let () =
  Command_unix.run
    (Command.group
       ~summary:"OCaml AWS command-line interface"
%s
    ])
;;
|}
    entries
;;
