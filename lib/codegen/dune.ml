open! Import

let dashes_to_underscores : string -> string =
  String.map ~f:(function
    | '-' -> '_'
    | c -> c)
;;

let make ~date ~service =
  let extra_deps =
    match service with
    | "s3" -> [ "core_unix"; "core_unix.time_unix" ]
    | _ -> []
  in
  let library =
    let service_unders =
      service
      |> String.map ~f:(function
           | '-' -> '_'
           | c -> c)
    in
    let extra_deps = String.concat ~sep:" " extra_deps in
    sprintf
      {|
  (library
    (name awso_%s)
    (public_name awso.%s)
    (libraries awso %s)
    (flags :standard)
    (ocamlopt_flags (-linscan))
    (preprocess (pps ppx_expect))
    (synopsis "OCaml AWS %s API.")
    (inline_tests))
       |}
      service_unders
      service
      extra_deps
      service
  in
  let endpoints_ml =
    sprintf
      {|
  (rule
    (targets endpoints.ml)
    (deps (:service %%{project_root}/vendor/botocore/botocore/data/%s/%s/service-2.json))
    (action (run %%{bin:awso-codegen} service endpoints
                  --impl endpoints.ml
                  --service %%{service})))
|}
      service
      date
  in
  let num_submodules =
    match service with
    | "ec2" -> 8
    | "ssm" | "iot" | "rds" | "sagemaker" -> 2
    | _ -> 0
  in
  let value_modules =
    num_submodules |> List.init ~f:(sprintf "values_%d.ml") |> String.concat ~sep:" "
  in
  let sub_flags =
    num_submodules
    |> List.init ~f:(sprintf "--sub values_%d.ml")
    |> String.concat ~sep:" "
  in
  let values_ml =
    sprintf
      {|
(rule
  (targets values.ml %s)
  (deps (:service %%{project_root}/vendor/botocore/botocore/data/%s/%s/service-2.json))
  (action (run %%{bin:awso-codegen} service values
                --service-id %s
                --impl values.ml %s
                --service %%{service})))
      |}
      value_modules
      service
      date
      service
      sub_flags
  in
  sprintf ";; do not edit! generated by awso-codegen\n%s\n%s\n%s" library endpoints_ml values_ml
;;

let has_addendum ~io_kind ~service =
  match io_kind with
  | `Async ->
    List.mem
      ~equal:String.equal
      [ "cognito-identity"
      ; "cognito-idp"
      ; "glue"
      ; "s3"
      ; "sqs"
      ; "sso"
      ; "sts"
      ]
      service
  | `Lwt -> false
;;

let make_io (io_kind : [ `Async | `Lwt ]) ~date ~service =
  let io =
    match io_kind with
    | `Async -> "async"
    | `Lwt -> "lwt"
  in
  let service_under =
    service
    |> String.map ~f:(function
         | '-' -> '_'
         | c -> c)
  in
  let preprocess =
    match io_kind with
    | `Async -> "\n  (preprocess (pps ppx_jane))\n  (inline_tests)"
    | `Lwt -> ""
  in
  let library =
    sprintf
      {|
(library
  (name awso_%s_%s)
  (flags (:standard))
  (public_name awso-%s.%s)
  (libraries awso.%s awso-%s)%s)
      |}
      service_under
      io
      io
      service
      service
      io
      preprocess
  in
  let num_submodules =
    match service with
    | "ec2" -> 3
    | "iot" | "rds" | "sagemaker" -> 2
    | _ -> 0
  in
  let cli_ml =
    match io_kind with
    | `Lwt -> ""
    | `Async ->
      let value_modules =
        num_submodules |> List.init ~f:(sprintf "cli_%d.ml") |> String.concat ~sep:" "
      in
      let maybe_submodule_flags =
        num_submodules
        |> List.init ~f:(sprintf "--sub cli_%d.ml")
        |> String.concat ~sep:" "
      in
      sprintf
        {|
(rule
(targets cli.ml %s)
(deps (:service %%{project_root}/vendor/botocore/botocore/data/%s/%s/service-2.json))
(action (run %%{bin:awso-codegen} service-io cli
 --impl cli.ml %s
 --service %%{service})))
    |}
        value_modules
        service
        date
        maybe_submodule_flags
  in
  let main_module = sprintf "awso_%s_%s" service_under io in
  let addendum = has_addendum ~io_kind ~service in
  let io_ml_mli =
    sprintf
      {|
(rule
(targets io.ml io.mli)
(deps (:service %%{project_root}/vendor/botocore/botocore/data/%s/%s/service-2.json))
(action (run %%{bin:awso-codegen} service-io io
  --service %%{service}
  --impl io.ml
  --intf io.mli
  --base-module Awso_%s
  --io-subsystem %s)))
    |}
      service
      date
      service_under
      io
  in
  let main_module_ml =
    if addendum
    then ""
    else
      let content =
        match io_kind with
        | `Async -> "include Io\\nmodule Values = Values\\nmodule Cli = Cli"
        | `Lwt -> "include Io\\nmodule Values = Values"
      in
      sprintf
        {|
(rule
 (targets %s.ml)
 (action (with-stdout-to %s.ml (echo "%s"))))
    |}
        main_module
        main_module
        content
  in
  let values_ml =
    sprintf
      {|
(rule
 (targets values.ml)
 (action (with-stdout-to %%{targets} (run %%{bin:awso-codegen} service-io values
   --service %s
 ))))
    |}
      service
  in
  sprintf
    ";; do not edit! generated by awso-codegen!\n%s\n%s\n%s\n%s\n%s"
    library
    cli_ml
    io_ml_mli
    main_module_ml
    values_ml
;;

let make_cli_async ~service =
  let service_ = dashes_to_underscores service in
  let executable =
    sprintf
      {|
  (executable
    (package awso-async)
    (name awso_%s)
    (flags (:standard))
    (public_name awso-%s)
    (libraries awso-async.%s awso-async core_unix.command_unix)
    (preprocess (pps ppx_jane)))
  |}
      service_
      service
      service
  in
  let script =
    let target = sprintf "awso_%s.ml" service_ in
    sprintf
      {|
(rule
  (targets %s)
  (action (with-stdout-to %%{targets} (run %%{bin:awso-codegen} cli script
    --service %s
  ))))|}
      target
      service
  in
  sprintf ";; do not edit! generated by awso-codegen!\n%s\n%s" executable script
;;
